package modelo;

public class Coordenada {

	private double x, y;

	public Coordenada(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return this.x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public double getY() {
		return this.y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
